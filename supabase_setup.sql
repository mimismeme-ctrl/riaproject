-- 1. books 테이블 생성
CREATE TABLE IF NOT EXISTS books (
  id            TEXT PRIMARY KEY,
  title         TEXT NOT NULL,
  author        TEXT,
  illustrator   TEXT,
  publisher     TEXT,
  year          INTEGER,
  age_range     TEXT,
  themes        TEXT[],
  keywords      TEXT[],
  synopsis      TEXT,
  cover_color   TEXT DEFAULT '#EEF3FF',
  cover_emoji   TEXT DEFAULT '📚',
  creative_questions TEXT[],
  kids_questions     TEXT[],
  is_featured   BOOLEAN DEFAULT false,
  lesson_tips   TEXT,
  created_at    TIMESTAMPTZ DEFAULT NOW(),
  updated_at    TIMESTAMPTZ DEFAULT NOW()
);

-- 2. Row Level Security 활성화
ALTER TABLE books ENABLE ROW LEVEL SECURITY;

-- 3. 정책 설정 (anon 키로 읽기/쓰기/수정/삭제 모두 허용)
DROP POLICY IF EXISTS "allow_select" ON books;
DROP POLICY IF EXISTS "allow_insert" ON books;
DROP POLICY IF EXISTS "allow_update" ON books;
DROP POLICY IF EXISTS "allow_delete" ON books;

CREATE POLICY "allow_select" ON books FOR SELECT USING (true);
CREATE POLICY "allow_insert" ON books FOR INSERT WITH CHECK (true);
CREATE POLICY "allow_update" ON books FOR UPDATE USING (true);
CREATE POLICY "allow_delete" ON books FOR DELETE USING (true);

-- 4. updated_at 자동 갱신 함수
CREATE OR REPLACE FUNCTION update_updated_at()
RETURNS TRIGGER AS $$
BEGIN
  NEW.updated_at = NOW();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE OR REPLACE TRIGGER set_updated_at
  BEFORE UPDATE ON books
  FOR EACH ROW EXECUTE FUNCTION update_updated_at();

